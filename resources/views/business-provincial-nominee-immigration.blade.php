                <h3>Prince Edward Island</h3>

                <strong>PEI uses programs to aim at business acumen, skilled workers and young graduates right out of
                    university or college. These different programs are used to accelerate skilled immigrants into permanent
                    residence. These streams are broken into 3 categories:</strong>

                <ul class="inner2">
                    <li>
                        <h3>PEI Express Entry</h3>
                        Prospective immigrants who have expressed an interest in living and working on Prince Edward Island
                        and are in the federal Express Entry pool may be eligible for PEI Express Entry. Candidates must be
                        eligible for one of the federal economic immigration classes, but only those with skills and
                        experience that meet the province's current labour needs will be considered for Express Entry PNP.
                    </li>

                    <li>
                        <h3>Labour Impact</h3>
                        Labour impact streams gives foreign workers and graduates who have already been hired by a PEI
                        employer to gain access to an accelerated path to permanent residency. Streams in Labour Impact
                        include: Skilled Workers in PEI, Skilled Workers Outside Canada, Critical Worker and International
                        Graduate.
                    </li>

                    <li>
                        <h3>Business Impact</h3>
                        Individuals desiring to immigrate to Canada on a business visa may take advantage of the PEI PNP's
                        Business Impact route, whether they are creating an enterprise with 100 percent ownership or
                        investing in a new or established PEI business. They may also choose to obtain a work visa by
                        investing in a PEI firm, which will allow them to settle into their new life in Canada and PEI more
                        quickly.
                    </li>
                </ul>

                <br>

                For more information regarding requirements and admissions for a particular stream in PEI, contact us at
                Just to Canada today.

