                Provincial Nominee Immigration or Provincial Nominee Programs (PNP) is a network of varied programs that run
                across Canada, supported by almost all provinces and territories. These programs allow these provinces to
                nominate skilled workers as candidates to be allowed into Canada. This varies for what skills are required
                in each economy that is nominating these skilled workers.
                <br>
                <br>

                <h3>SPONSORED EMPLOYEES</h3>

                For some of these programs, the employer nominates their candidates and after approval from the province,
                are run through an expedited process. The early stages see these skilled workers obtain temporary work
                permits to enter Canada while simultaneously being processed for permanent residence. Outside of a few
                provinces (Saskatchewan, Quebec and Manitoba), skilled based provincial programs usually need an employer to
                sponsor an applicant. Without government approval by said province, the application will be denied. This is
                usually caused when an employer does not have approval for sponsorship by the government.
                <br>
                <br>

                Sponsoring employers must show efforts to hire local workers and meet varied terms and conditions that exist
                in the occupation. These efforts help qualify foreign workers, and is sometimes labeled a transition plan.
                To become a sponsored employee, skill level must meet Type 0 or Skill Level A/B on the National Occupation
                Classification (NOC).
                <br>
                <br>

                <h3>HOW TO APPLY</h3>

                <strong>Find the right Program</strong><br>
                Reflect on the available provinces and territories, and see which fits best for you. Requirements and
                eligibility factor by province, so it is important to see which fits your criteria.
                <br>
                <br>

                <strong>Apply</strong><br>
                We will be happy to assist you with the submission of your application to the PNP office, as applicable and
                where you qualify.
                <br>
                <br>

                <strong>Getting a Provincial Nomination Certificate</strong><br>
                Following a successful application, you will be nominated for an application to become a permanent resident
                of Canada
                <br>
                <br>

                <strong>Submit Permanent Residence Application</strong><br>
                Apply for permanent residence to the Canadian federal government. This can be done by 2 processes, depending
                on the PNP you have chosen.

