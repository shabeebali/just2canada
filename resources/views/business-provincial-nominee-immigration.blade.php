                <h3>Alberta</h3>

                <strong>Alberta is the center of agricultural production in Canada. This makes Alberta unique in the
                    pathways it has. Alberta operates its streams throughout 3 major categories:</strong>

                <ul class="inner2">
                    <li>
                        <h3>Alberta Opportunity Stream</h3>
                        This stream targets individuals who are already working presently in Alberta and have an offer of
                        employment to continue to do so full-time. The eligibility requirements are work experience centric,
                        with 12 months minimum of work experience in Alberta, along with a withstanding job offer and a
                        language level of CLB 4.
                    </li>
                    <li>
                        <h3>Alberta Express Entry Stream</h3>
                        This stream allows the province to make nominations to certain candidates with active profiles in
                        the Express Entry system. The exact selection process in unclear but considerations correlate to
                        those with strong ties to Alberta and have the ability to help expand diversification and economic
                        growth.
                    </li>

                    <li>
                        <h3>Alberta Self-Employed Farmer Stream</h3>
                        This stream zeroes in on immigrants who are seasoned in farming operations. This would require
                        having sufficient funds to launch a farming operation in Alberta. A minimum investment of $500,000
                        CAD is required as well as a detailed plan laying out the launch of the farm.
                    </li>
                </ul>

                <br>

                For more information regarding requirements and admissions for a particular stream in Alberta, contact us at
                Just to Canada today.
