                <h3>Nova Scotia</h3>

                <strong>Nova Scotia is bordering on the Atlantic Ocean, making it an ideal location for immigrants to fill
                    in the workforce and help grow Nova Scotia both on an economical and communal level. Nova Scotia
                    operates on multiple streams including:</strong>

                <ul class="inner2">
                    <li>
                        <h3>Nova Scotia Demand: Express Entry</h3>
                        This particular stream is looking for highly skilled individuals who will be able to join and
                        contribute to the Nova Scotia economy. Candidates must have either a job offer from a Nova Scotia
                        employer or work experience in one of Nova Scotia's in-demand occupations to qualify. This program
                        is linked to the federal Express Entry immigration system, allowing successful applicants to have
                        their immigration applications processed more quickly.
                    </li>
                    <li>
                        <h3>Nova Scotia Experience: Express Entry</h3>
                        The Nova Scotia Experience: Express Entry stream is designed for highly skilled individuals who want
                        to live and work permanently in Nova Scotia. Candidates must have at least one year of experience
                        working in a highly qualified occupation in Nova Scotia to be eligible. This program is associated
                        to the federal Express Entry immigration system, enabling successful applicants to have their
                        immigration applications processed more quickly.
                    </li>

                    <li>
                        <h3>Nova Scotia Skilled Workers</h3>
                        The Nova Scotia Skilled Worker stream allows Nova Scotia firms to hire foreign workers and recently
                        graduated international students whose talents are in high demand in the province. If a skilled
                        worker receives a job offer from a Nova Scotia employer, he or she may be able to apply through this
                        stream to expedite their immigration process.
                    </li>
                    <li>
                        <h3>Nova Scotia Entrepreneur</h3>
                        The Nova Scotia Entrepreneur Stream is designed for experienced business owners or senior business
                        managers who want to invest in and run a Nova Scotia firm. Applicants must have a considerable net
                        worth and be ready to use their business management experience to start a new firm or purchase an
                        existing business in Nova Scotia in order to qualify. After one year of conducting a business in
                        Nova Scotia, applicants may be nominated for permanent residence status.
                    </li>

                    <li>
                        <h3>Nova Scotia Labour Market Priorities</h3>
                        This stream seeks Express Entry candidates with work experience in particularly in-demand positions
                        in the province Currently, this stream is only accepting applications from Early Childhood Educators
                        and Assistants (NOC 4214).
                    </li>

                    <li>
                        <h3>Atlantic Immigration Program (AIPP)</h3>
                        This program is an initiative created by Canada’s Atlantic provinces and the Canadian federal
                        government. This process is for those who have obtained a job offer from an employer in the Atlantic
                        region.
                    </li>
                </ul>

                <br>

                For more information regarding requirements and admissions for a particular stream in Nova Scotia, contact
                us at Just to Canada today.
