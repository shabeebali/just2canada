                <h3>Manitoba</h3>

                <strong>Manitoba looks at economic development and labour force needs as ideal areas of enhancement, making
                    it a great location for immigration. Manitoba’s pathways look specifically at skilled workers,
                    entrepreneurs and workers in sectors with labour shortages, recent graduates and business-minded
                    agricultural specialists. These streams are broken down into 4 major categories:</strong>

                <ul class="inner2">
                    <li>
                        <h3>Skilled Workers in Manitoba</h3>
                        Skilled Worked in Manitoba looks at skilled workers that have work experience or have a job offer in
                        Manitoba. Applicants must be currently working in Manitoba during the application process. Some
                        cases of consideration occur for those who have attended a recruitment session in their foreign
                        nation. The streams under Skilled Workers in Manitoba include: Skilled Workers in Manitoba Stream,
                        Manitoba Experience Pathway, Employer Direct Recruitment Pathway
                    </li>

                    <li>
                        <h3>Skilled Workers Overseas</h3>
                        This category focuses on workers who are experienced to join the work force and have the
                        adaptability to easily transition to live in Manitoba. Workers with experience in Manitoba’s
                        In-Demand Occupations are prioritized in this category. Streams in Skilled Workers Overseas include:
                        Manitoba Skilled Workers Overseas Stream, Manitoba Express Entry Pathway and Manitoba Human Capital
                        Pathway.
                    </li>

                    <li>
                        <h3>International Education Stream (IES)</h3>
                        IES targets those who have recently graduated from post-secondary within Manitoba. It is also
                        required for candidates to have some work experience or business management expertise. This can be
                        completed following their graduation, or as a part of the co-op they completed during their time of
                        study. The streams in IES include: Manitoba Career Employment Pathway, Manitoba Graduate Internship
                        Pathway and Manitoba Student Entrepreneur Pathway.
                    </li>

                    <li>
                        <h3>Business Investor Stream (BIS)</h3>
                        The BIS category focuses on individuals looking to launching a new business in Manitoba. The two
                        streams look at different business models, one focusing on a general model while the other hones in
                        on the agricultural sector. The two streams in BIS include: Manitoba Entrepreneur Pathway and
                        Manitoba Farm Investor Pathway.
                    </li>
                </ul>
                For more information regarding requirements and admissions for a particular stream in Manitoba, contact us
                at Just to Canada today.
