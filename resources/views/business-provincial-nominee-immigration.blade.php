                <h3>British Columbia</h3>

                <strong>British Columbia looks at economic development and labour force needs as ideal areas of enhancement,
                    making it a great location for immigration. BC’s pathways look specifically at skilled workers,
                    entrepreneurs and workers in sectors with labour shortages. The streams are divided into 2 major
                    categories:</strong>

                <ul class="inner2">
                    <li>
                        <h3>British Columbia Skills Immigration</h3>
                        Skills Immigration aims at skilled workers who could be capable members of the workforce in British
                        Columbia. The pathways through this category looks at immigrants who have prospected employment
                        opportunities and have experience indicating they can maintain employment consistently. The streams
                        through BC Skills Immigration include: BC Skilled Worker, BC Healthcare Professional, BC
                        International Graduate, BC International Post-Graduate and BC Entry Level &amp; Semi-Skilled Worker.
                    </li>
                    <li>
                        <h3>British Columbia Entrepreneur Immigration</h3>
                        This category targets those with a solid background in running a successful business. Whether that
                        be from an ownership, investment or management position. High net worth and corporations that prove
                        high revenues have the ability to immigrate and start a business or enterprise in British Columbia.
                        The streams that follow under BC Entrepreneur Immigration include: BC Entrepreneur, BC Entrepreneur
                        Regional Pilot and BC Strategic Pilots.
                    </li>
                </ul>

                For more information regarding requirements and admissions for a particular stream in British Columbia,
                contact us at Just to Canada today.
