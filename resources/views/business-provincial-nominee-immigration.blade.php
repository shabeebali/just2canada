                <h3>New Brunswick</h3>

                <strong>New Brunswick is bordering on the Atlantic Ocean, making it an ideal location for immigrants to fill
                    in the workforce and help grow New Brunswick both on an economical and communal level. New Brunswick
                    operates on 4 major streams:</strong>

                <ul class="inner2">
                    <li>
                        <h3>New Brunswick Express Entry Labour Market Stream</h3>
                        This stream acts as a direct pathway to permanent residence. It is aligned with Express Entry and
                        mainly targets highly skilled individual. The process is expedited with those who have successful
                        applications on their Express Entry profiles.
                    </li>
                    <li>
                        <h3>Post-Graduate Entrepreneurial Stream</h3>
                        Aimed at recent graduates of New Brunswick universities and colleges who have started or purchased a
                        New Brunswick business and have run it for at least a year If an applicant meets these requirements
                        and intends to settle permanently in New Brunswick, they may be eligible for a provincial nomination
                        through this stream.
                    </li>
                    <li>
                        <h3>Skilled Workers with Employer Support Stream</h3>
                        Looks at high skilled foreign workers with a current job offer from a New Brunswick employer.
                    </li>

                    <li>
                        <h3>Atlantic Immigration Pilot Program (AIPP)</h3>
                        This program is an initiative created by Canada’s Atlantic provinces and the Canadian federal
                        government. This process is for those who have obtained a job offer from an employer in the Atlantic
                        region.
                    </li>
                </ul>

                <br>

                For more information regarding requirements and admissions for a particular stream in New Brunswick, contact
