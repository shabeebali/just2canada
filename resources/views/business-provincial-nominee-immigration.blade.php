                <h2>PROVINCIAL NOMINEE PROGRAMS BY PROVINCE</h2>

                <h3>Ontario</h3>
                <strong>Ontario’s PNPs works as pathways to bring in immigrants that can prove beneficial in areas such as:
                    skilled trades, working in sectors with labour shortages, investors and entrepreneurs. The province
                    divides streams into 3 major categories that include:</strong>

                <ul class="inner2">
                    <li>
                        <h3>Ontario Human Capital</h3>
                        This section focuses on targeting the labour force. Skills and experience that work through this
                        stream include being proficient in English and/or French, having work or educational experience in
                        Ontario and having valued Express Entry profiles. Streams under Ontario Human Capital include:
                        Masters Graduate, PhD Graduate, Human Capital Priorities, Skilled Trades and French-Speaking Skilled
                        Worker.
                    </li>

                    <li>
                        <h3>Ontario Employer Job Offer</h3>
                        Streams under this category are for individuals who have a valid job offer pending from an Ontario
                        employer. Both the employer and job must meet certain requirements for validity and the applicant
                        must meet certain eligibility requirements varying on the stream. Streams under Ontario Employer Job
                        Offer include: Foreign Worker, International Student with a Job Offer and In-Demand Skills.
                    </li>

                    <li>
                        <h3>Ontario Business</h3>
                        Ontario Business immigration hones in on applicants interested in making corporate or
                        entrepreneurial investment in business development in Ontario. As of right now, the Corporate and
                        Investor streams have been closed making the Entrepreneur stream the only available stream currently
                        in this category.
                    </li>
                </ul>

                <br>

                For more information regarding requirements and admissions for a particular stream in Ontario, contact us at
                Just to Canada today.

                <br>
