                Canada’s Start Up Visa (CSUV) program allows business minds and entrepreneurs to immigrate over to Canada to
                start a new business. Entrepreneurs are tasked with exemplifying why they believe they should be allowed to
                venture into new business in Canada which includes job creation for Canadians, being globally competitive
                and demonstrating that the business is innovative. While not the easiest of tasks, talented entrepreneurs
                can use this program to help jump start their businesses in Canada.
