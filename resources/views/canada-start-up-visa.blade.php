                <h3>DESIGNATED ORGANIZATIONS/ENTITIES</h3>

                Apart of being eligible for CSUV is proof of a Commitment Certificate and Letter of Support from a
                designated organization. This proof will show backing in your business, and there are different standards
                depending on what type of entity decides to take on your business. Applicants are not required to invest any
                of their own capital.
                <br>
                <br>

                If the designated organization is an angel investor group, they’re required to invest a minimum of $75,000
                into the business. It is also possible to qualify multiple angel investors, to achieve the $75,000 minimum
                that is required.
                <br>
                <br>

                If the designated organization is a venture capital fund, an investment of $200,000 is required at the
                minimum. Again, multiple commitments can be agreed upon to reach the $200,000 required.
                <br>
                <br>

                For business incubators, the applicant's business must be accepted into the business incubator program.
                <br>
                <br>

                <h3>HOW TO APPLY</h3>

                The start-up visa application begins with filling out a generic application form for Canada. Once all
                required fields are filled, click the “Validate” button and print the form including the barcode page. Make
                sure your signature and date are on the form.
                <br>
                <br>

                Complete the document checklist. This checklist must be completed and signed and acts as the cover page to
                your application. From here you must pay your application fees. This depends on what type of applicant it is
                for the business in regards to processing fees. An individual’s processing fee is $85, while families
                applying together can be up to $170 and groups of 3 or more performers can be up to $255. Other fees are
                also charged which include biometric fees, medical exams, police certificates, right of permanent residence
                fee, and language test fee.
                <br>
                <br>

                Following this is the submission of the application, all documents should be put in a 9” x 12” envelope and
                mailed to the correct address. The process for the start-up visa takes anywhere between 12 to 16 months to
                be completed. Our professionals at Just To Canada can make the process for a start-up Visa no only
                attainable, but help kickstart the vision for all foreign entrepreneurs and the business they dream about
