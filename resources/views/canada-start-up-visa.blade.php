                <h2>REQUIREMENTS</h2>

                <strong>The requirements for a start-up visa include 4 integral things:</strong>

                <h3>Business must be qualified</h3>
                <ul class="inner2">
                    <li>You must have a minimum of 10% of the voting rights attached to all shares of said business</li>
                    <li>Along with the designated organization, you must hold a minimum of 50% of the voting rights attached
                        to all shares of the company
                    </li>
                </ul>
                <br>

                <h3>Commitment Certificate and Letter of Support from designated organization</h3>
                <ul class="inner2">
                    <li>Must have backing from a valued designated organization or private sector investor. This can be an
                        angel investor, venture capital fund or business incubator</li>
                    <li>Must pitch business idea to said entities, if successful; you will receive a letter of support
                    </li>
                </ul>
                <br>


                <h3>Meeting the language proficiency standards</h3>
                <ul class="inner2">
                    <li>Must be at the minimum of Level 5 on the Canadian Language Benchmark (CLB) in either English or
                        French for all linguistic practices
                    </li>
                </ul>
                <br>

                <h3>Proof of funds</h3>
                <ul class="inner2">
                    <li>Funds must be sufficient, readily available and transferable</li>
